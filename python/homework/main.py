from bs4 import BeautifulSoup

mainAddr = 'https://www.zdnet.com'
latest = '/topic/?filter=latest'
resp = requests.get(mainAddr+latest)
print("\n\n")
print("--------------------------------------------------------------------------------------------------------------------------------")
print("----------------------------------             Z D N E T                                   -------------------------------------")
print("----------------------------------                  L A T E S T      I S S U E S           -------------------------------------")
print("--------------------------------------------------------------------------------------------------------------------------------\n\n")



print("--------------------------------------------------------------------------------------------------------------------------------")
print("--------------------------------------------------------------------------------------------------------------------------------")
if(resp.status_code==requests.codes.ok):
    html = resp.text
    soup = BeautifulSoup(html, 'html.parser')
    items = soup.find_all("article","item")
    for aItem in items:
        # print(aItem)
        print("TITLE : ", aItem.find("a", "thumb").get('title'))
        print("ADDRESS : ", mainAddr + aItem.find("a", "thumb").get('href'), "\n")
        print(aItem.find("p", "summary").get_text())
        print("================================================================================================================================")
else:
    print(resp.status_code,'접속실패')
